import math


##      math Greatest Common Denominator      ##
gcd = math.gcd(24, 36)
print(gcd)
# Output: 12


##      math Factorial      ##
print(math.factorial(5))
# Output: 120


##      math Power      ##
print(math.pow(2, 3))
# Output: 8.0


##      math Square Root      ##
print(math.sqrt(16))
# Output: 4.0


##      math Square Root      ##
angle = math.radians(45)  # Converts degrees to radians
print(math.sin(angle))
# Output: 0.7071067811865476
print(math.cos(angle))
# Output: 0.52532198881